#include <stdio.h>

int main() {
    int a = 10, b = 20;

    printf("Relational Operators Example\n\n");
    printf("0 is False and 1 is True\n\n");

    printf("a == b : %d\n", a == b);
    printf("a != b : %d\n", a != b);
    printf("a > b  : %d\n", a > b);
    printf("a < b  : %d\n", a < b);
    printf("a >= b : %d\n", a >= b);
    printf("a <= b : %d\n", a <= b);

    return 0;
}
